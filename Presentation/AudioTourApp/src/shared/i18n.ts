// Fallback chain: lang → 'en' → key itself
export function t(lang: LangCode, key: string): string {
  return translations[lang]?.[key]
    ?? translations['en']?.[key]
    ?? key;
}

// Hook-friendly version
export function useT(lang: LangCode) {
  return (key: string) => t(lang, key);
}